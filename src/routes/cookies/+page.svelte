Diese Cookie-Richtlinie erklärt, wie [Deine Website] Cookies und ähnliche Technologien für Analysezwecke verwendet. Diese Richtlinie sollte in Verbindung mit unserer Datenschutzrichtlinie gelesen werden.

1. Was sind Cookies?

Cookies sind kleine Textdateien, die auf deinem Gerät gespeichert werden, wenn du unsere Website besuchst. Sie dienen dazu, die Benutzererfahrung zu verbessern, indem sie Informationen über deine Aktivitäten auf der Website speichern.

2. Welche Cookies verwenden wir?

Wir verwenden nur Cookies, die für das Funktionieren der Website und zur Durchführung von Analysen notwendig sind. Insbesondere verwenden wir Google Analytics, einen Webanalysedienst von Google Inc. („Google“). Google Analytics verwendet Cookies, um statistische Daten über die Nutzung unserer Website zu sammeln.

3. Wie verwenden wir Google Analytics?

Google Analytics hilft uns, Informationen darüber zu sammeln, wie Besucher unsere Website nutzen. Die gesammelten Daten sind anonymisiert und enthalten keine persönlich identifizierbaren Informationen. Die Informationen werden verwendet, um Berichte über die Website-Aktivitäten zu erstellen und die Website weiter zu verbessern.

4. Deine Zustimmung zu Cookies

Beim ersten Besuch unserer Website wird dir ein Cookie-Banner angezeigt. Durch Klicken auf "Einverstanden" stimmst du der Verwendung von Cookies, einschließlich Google Analytics, gemäß dieser Cookie-Richtlinie zu. Du kannst Cookies auch über die Einstellungen deines Browsers deaktivieren.

5. Deaktivierung von Cookies

Du kannst die Verwendung von Cookies jederzeit über die Einstellungen deines Browsers deaktivieren. Beachte jedoch, dass dies die Funktionalität der Website beeinträchtigen kann.

6. Kontaktinformationen

Wenn du Fragen zu unserer Cookie-Richtlinie hast, kontaktiere uns bitte unter [deine Kontaktdaten].

